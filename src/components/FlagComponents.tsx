// Helper function to get flag component for a league
export function getLeagueFlag(leagueName: string) {
  switch (leagueName) {
    case 'UEFA Champions League':
    case 'UEFA Europa League':
    case 'UEFA Conference League':
      return <EuEurope />;
    case 'Premier League':
      return <GbEngEngland />;
    case 'Serie A':
      return <ItItaly />;
    case 'Bundesliga':
      return <DeGermany />;
    case 'La Liga':
      return <EsSpain />;
    default:
      return null;
  }
}